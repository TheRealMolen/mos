#pragma once

#include <cstdint>

//----------------------------------------------------------------------------------------------

using u32 = uint32_t;
using uptr = uintptr_t;

//----------------------------------------------------------------------------------------------

template<typename T>
inline T clamp(T val, T lo, T hi)
{
    if (val <= lo)
        return lo;
    if (val >= hi)
        return hi;
    return val;
}

